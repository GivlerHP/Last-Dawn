package ru.givler.lastdawn.sanity;

/**
 * Научная типология стадий рассудка.
 */
public enum SanityStage {
    SANE,         // 0–25: Нормальное состояние
    ANXIOUS,      // 26–50: Тревожность
    PSYCHOSIS,    // 51–75: Психоз (галлюцинации)
    INSANITY      // 76–100: Сумасшествие (критическая стадия)
}